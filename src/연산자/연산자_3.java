package 연산자;

public class 연산자_3 {
    public static void main(String[] args) {
        int a = 10, b = 10;

        System.out.println("최초값 : a =" + a + ", b = " + b);

        System.out.println("\na++ = " + a++ + " ++b =" + ++b);
        System.out.println("실행 후 a = " + a + ", b = " + b);

        System.out.println("\na-- = " + a-- + " --b =" + --b);
        System.out.println("실행 후 a = " + a + ", b = " + b);
    }
}
