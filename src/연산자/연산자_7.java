package 연산자;

public class 연산자_7 {
    public static void main(String[] args) {
        int a = 0, b = 1, c = 2;

        if(a != 0 && b != 0)
        {
            System.out.print("1 ");
        }
        else
        {
            System.out.print("0 ");
        }
        if(a != 0 || b != 0)
        {
            System.out.print("1 ");
        }
        else
        {
            System.out.print("0 ");
        }
        if(b != 0 && c != 0)
        {
            System.out.print("1 ");
        }
        else
        {
            System.out.print("0 ");
        }
        if(a == 0)
        {
            System.out.print("1 ");
        }
        else
        {
            System.out.print("0 ");
        }
    }
}
