package 반복제어문;

public class loop2_8 {
    public static void main(String[] args) {
        for (int i = 1; i <= 9; i++) {
            for (int j = 2; j <= 4; j++) {
                System.out.print(j + " * " + i + " = " + i*j + " ");
            }
            System.out.println();

        }
    }
}
